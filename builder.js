// Render the checklist from data.js
function renderDestinationsChecklist() {
  const container = document.getElementById("destinations-checklist-container");
  if (!container) return;
  
  let html = "";
  
  destinationsGuide.forEach(dest => {
    // Determine category icon and details
    let icon = "fa-map-pin";
    if (dest.id === "hanoi") icon = "fa-city";
    if (dest.id === "sapa") icon = "fa-mountain";
    if (dest.id === "halong") icon = "fa-ship";
    if (dest.id === "ninhbinh") icon = "fa-water";
    if (dest.id === "danang_hoian") icon = "fa-umbrella-beach";
    
    html += `
      <div class="filter-group">
        <h3><i class="fa-solid ${icon} text-emerald"></i> ${dest.name}</h3>
        <div class="checkbox-list">
    `;
    
    dest.thingsToDo.forEach((thing, idx) => {
      // Clean up markdown bold markers for simple text display
      const cleanText = thing.replace(/\*\*(.*?)\*\*/g, '$1');
      const uniqueId = `act-${dest.id}-${idx}`;
      
      html += `
        <label class="checkbox-item" for="${uniqueId}">
          <input type="checkbox" id="${uniqueId}" data-dest="${dest.id}" data-text="${cleanText}" onchange="toggleActivity('${uniqueId}', '${dest.id}')">
          <span>${cleanText}</span>
        </label>
      `;
    });
    
    html += `
        </div>
      </div>
    `;
  });
  
  container.innerHTML = html;
}

// Track checked activities and destinations
function toggleActivity(id, destId) {
  const checkbox = document.getElementById(id);
  if (checkbox.checked) {
    selectedActivities.add(id);
    selectedDestinations.add(destId);
  } else {
    selectedActivities.delete(id);
    
    // Check if any other activity for this destination is still checked
    const anyLeft = Array.from(selectedActivities).some(actId => actId.startsWith(`act-${destId}-`));
    if (!anyLeft) {
      selectedDestinations.delete(destId);
    }
  }
}

