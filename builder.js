// 5. Sharing & URL Parameters
// ==========================================
function shareRoute() {
  const s = selectedDestinations.has("sapa") ? "1" : "0";
  const n = selectedDestinations.has("ninhbinh") ? "1" : "0";
  const h = selectedDestinations.has("halong") ? "1" : "0";
  const c = selectedDestinations.has("danang_hoian") ? "1" : "0";
  
  // Convert selected activities set to comma-separated list
  const acts = Array.from(selectedActivities).join(",");
  const pace = document.querySelector('input[name="trip-pace"]:checked').value;
  
  const url = new URL(window.location.href);
  url.searchParams.set("route", `${s}-${n}-${h}-${c}`);
  url.searchParams.set("acts", acts);
  url.searchParams.set("pace", pace);
  
  navigator.clipboard.writeText(url.toString()).then(() => {
    alert("קישור אישי למסלול הועתק ללוח! תוכלו לשתף אותו עם שאר בני המשפחה.");
  }).catch(err => {
    console.error("Failed to copy link: ", err);
    alert(`העתיקו את הכתובת הזו:\n${url.toString()}`);
  });
}

function loadSharedRoute() {
  const urlParams = new URLSearchParams(window.location.search);
  const routeParam = urlParams.get("route");
  const actsParam = urlParams.get("acts");
  const paceParam = urlParams.get("pace");
  
  if (!routeParam) return;
  
  // Restore pace radio button
  if (paceParam) {
    const radio = document.querySelector(`input[name="trip-pace"][value="${paceParam}"]`);
    if (radio) radio.checked = true;
  }
  
  // Restore checked activities
  if (actsParam) {
    const acts = actsParam.split(",");
    acts.forEach(id => {
      const checkbox = document.getElementById(id);
      if (checkbox) {
        checkbox.checked = true;
        
        // Trigger toggleActivity logic manually
        selectedActivities.add(id);
        const destId = checkbox.getAttribute("data-dest");
        selectedDestinations.add(destId);
      }
    });
  }
  
  // Auto-generate the route
  generateRoute();
}
