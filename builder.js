// 4. Custom Route Generation Logic
// ==========================================
function generateRoute() {
  const warningBox = document.getElementById("builder-warning-box");
  const warningText = document.getElementById("builder-warning-text");
  
  warningBox.style.display = "none";
  
  if (selectedDestinations.size === 0 || (selectedDestinations.size === 1 && selectedDestinations.has("hanoi"))) {
    alert("אנא בחרו לפחות פעילות אחת מחוץ להאנוי כדי להרכיב מסלול.");
    return;
  }
  
  // Build a binary combination string of: sapa-ninhbinh-halong-danang_hoian
  const s = selectedDestinations.has("sapa") ? "1" : "0";
  const n = selectedDestinations.has("ninhbinh") ? "1" : "0";
  const h = selectedDestinations.has("halong") ? "1" : "0";
  const c = selectedDestinations.has("danang_hoian") ? "1" : "0";
  
  const key = `${s}-${n}-${h}-${c}`;
  const routeData = customItineraries[key];
  
  if (!routeData) {
    alert("שגיאה בחישוב המסלול. אנא נסו לבחור שילוב אחר.");
    return;
  }
  
  // Render warning if present
  if (routeData.warning) {
    warningBox.style.display = "flex";
    warningText.innerText = routeData.warning;
  }
  
  // Update UI Stats
  document.getElementById("active-route-badge").innerText = "מסלול מותאם אישית";
  document.getElementById("active-route-title").innerText = routeData.title;
  document.getElementById("active-route-subtitle").innerText = routeData.subtitle;
  
  const pace = document.querySelector('input[name="trip-pace"]:checked').value;
  let pacingText = routeData.stats.pacing;
  let parentsText = routeData.stats.suitability;
  
  if (pace === "relaxed") {
    pacingText = "רגוע ומותאם אישית";
    parentsText = "⭐⭐⭐⭐⭐ (גבוהה מאוד)";
  } else if (pace === "active") {
    pacingText = "קצב מהיר וספורטיבי";
    parentsText = "⭐⭐⭐ (בינונית - דורש כושר)";
  }
  
  document.getElementById("stat-flights").innerText = routeData.stats.flights;
  document.getElementById("stat-road").innerText = routeData.stats.roadHours;
  document.getElementById("stat-pacing").innerText = pacingText;
  document.getElementById("stat-parents").innerText = parentsText;
  
  // Render Timeline
  renderItinerary(routeData.itinerary);
  
  // Update Map
  updateMapForRoute(routeData.path, routeData.segments);
  
  // Enable print/share buttons
  document.getElementById("share-route-btn").removeAttribute("disabled");
  document.getElementById("print-route-btn").removeAttribute("disabled");
}

function renderItinerary(itineraryList) {
  const timeline = document.getElementById("days-timeline");
  if (!timeline) return;
  
  timeline.innerHTML = itineraryList.map((item) => {
    // Check if any checked activities belong to this day's location
    const matchedActivities = Array.from(selectedActivities).map(actId => {
      const checkbox = document.getElementById(actId);
      return checkbox ? checkbox.getAttribute("data-text") : "";
    }).filter(text => {
      if (!text) return false;
      const lowerDesc = item.desc.toLowerCase();
      const lowerTitle = item.title.toLowerCase();
      const lowerText = text.toLowerCase().substring(0, 10); // Match partial strings
      return lowerDesc.includes(lowerText) || lowerTitle.includes(lowerText);
    });
    
    let badgeHtml = "";
    if (matchedActivities.length > 0) {
      badgeHtml = `
        <div style="margin-top: 8px;">
          ${matchedActivities.map(act => `<span class="checked-badge"><i class="fa-solid fa-star"></i> בחרתם: ${act}</span>`).join(" ")}
        </div>
      `;
    }
    
    return `
      <div class="timeline-item">
        <div class="timeline-circle"></div>
        <div class="timeline-content">
          <div class="timeline-day-header">
            <span class="day-num">יום ${item.day}</span>
            <span class="day-date">${item.date}</span>
          </div>
          <h4>${item.title}</h4>
          <p>${item.desc}</p>
          ${badgeHtml}
        </div>
      </div>
    `;
  }).join("");
}

